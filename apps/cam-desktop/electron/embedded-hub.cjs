async function _execCamcOnContext(ctx, camcArgs, { overrides = {}, timeoutMs = SYNC_DEFAULT_TIMEOUT_MS } = {}) {
  if (!ctx || !ctx.machine || ctx.machine.type !== 'ssh') {
    return { ok: false, error: 'not_ssh', detail: `context "${(ctx && ctx.name) || '?'}" is not an SSH context` };
  }
  if (!_sshTransport || typeof _sshTransport.execRemote !== 'function') {
    return { ok: false, error: 'ssh_transport_unavailable', detail: 'embedded Hub has no SSH transport configured' };
  }
  const m = ctx.machine;
  const baseOpts = {
    host:        m.host,
    user:        m.user,
    port:        m.port || 22,
    auth_method: m.auth_method || (m.key_file ? 'key' : 'agent'),
    key_file:    m.key_file || '',
    timeout_ms:  timeoutMs,
  };
  if (baseOpts.auth_method === 'password') {
    const pw = (typeof overrides.password === 'string' && overrides.password)
      ? overrides.password
      : _credentialFor(ctx);
    if (pw == null || pw === '') {
      return { ok: false, error: 'credential_missing', detail: 'password auth is configured but no remembered or one-shot password is available' };
    }
    baseOpts.password = pw;
  } else if (baseOpts.auth_method === 'key') {
    const pp = (typeof overrides.passphrase === 'string' && overrides.passphrase)
      ? overrides.passphrase
      : _credentialFor(ctx);
    if (pp != null) baseOpts.passphrase = pp;
  }
  const ready = await _ensureRemoteCamc(baseOpts);
  if (!ready.ok) {
    return { ok: false, error: ready.error || 'remote_camc_unavailable', detail: ready.detail || 'failed to prepare ~/.cam/camc on remote host' };
  }
  const argStr = camcArgs.map(a => String(a)).join(' ');
  const res = await _sshTransport.execRemote({ ...baseOpts, command: `${REMOTE_CAMC} ${argStr}` });
  if (!res || !res.ok) {
    const err = res || {};
    let code = err.error || 'exec_failed';
    if (code === 'remote_nonzero' && /not found|No such file/i.test(err.detail || err.stderr || '')) {
      code = 'camc_missing';
    }
    return { ok: false, error: code, detail: err.detail || err.stderr || 'unknown exec failure', stderr: err.stderr || '', stdout: err.stdout || '' };
  }
  return { ok: true, stdout: String(res.stdout || ''), stderr: String(res.stderr || '') };
}

/* ─────────────── SSH config parser (read-only, no shell) ─────────────── */

function expandHome(p) {
  if (!p) return p;
  if (p === '~' || p.startsWith('~/') || p.startsWith('~\\')) {
    return path.join(os.homedir(), p.slice(1).replace(/^[/\\]/, ''));
  }
  return p;
}

function sshConfigPaths() {
  const out = [];
  const home = os.homedir();
  if (home) out.push(path.join(home, '.ssh', 'config'));
  // Windows ssh-agent / OpenSSH also reads %USERPROFILE%\.ssh\config
  // which is what os.homedir() returns there, so the entry above
  // already covers it. Keeping it as a single path simplifies the
  // policy (one well-known location, no surprises).
  return out;
}

/** Parse a small, conservative subset of OpenSSH client config:
 *  - Host blocks (only entries WITHOUT wildcards `*` or `?`)
 *  - Per-block: HostName, User, Port, IdentityFile, ProxyJump (info only)
 *  - Lines starting with `#` are comments
 *  - `Include` directives are NOT followed in this pass
 *  Returns an array of { alias, hostName, user, port, identityFile }
 *  with identityFile as the raw `IdentityFile` reference (expanded
 *  via ~). Never reads key file contents. */
function parseSshConfig(text) {
  const lines = String(text || '').split(/\r?\n/);
  const blocks = [];
  // OpenSSH applies subsequent options to ALL aliases listed on the
  // current `Host` line (e.g. `Host alpha beta` followed by
  // `HostName h.example` configures BOTH alpha and beta). We track
  // the set of currently-open block objects as `active` and apply
  // each key/value to every one of them.
  let active = [];
  for (const raw of lines) {
    const line = raw.replace(/^\s+|\s+$/g, '');
    if (!line || line.startsWith('#')) continue;
    const m = /^([A-Za-z]+)[\s=]+(.+)$/.exec(line);
    if (!m) continue;
    const key = m[1].toLowerCase();
    const value = m[2].trim();
    if (key === 'host') {
      // Reset the active set to the concrete aliases on this Host
      // line. Wildcards (`*`, `?`) are skipped entirely — they're
      // OpenSSH defaults, not concrete hosts the user can pick.
      active = [];
      for (const alias of value.split(/\s+/)) {
        if (!alias || alias.includes('*') || alias.includes('?')) continue;
        const blk = {
          alias,
          hostName:     null,
          user:         null,
          port:         null,
          identityFile: null,
        };
        blocks.push(blk);
        active.push(blk);
      }
      continue;
    }
    if (active.length === 0) continue;
    let val;
    switch (key) {
      case 'hostname':
        for (const blk of active) blk.hostName = value;
        break;
      case 'user':
        for (const blk of active) blk.user = value;
        break;
      case 'port': {
        const p = Number.parseInt(value, 10);
        if (!Number.isFinite(p) || p < 1 || p > 65535) break;
        for (const blk of active) blk.port = p;
        break;
      }
      case 'identityfile':
        val = expandHome(value);
        for (const blk of active) blk.identityFile = val;
        break;
      default: break;
    }
  }
  return blocks;
}

function sshConfigHosts() {
  const paths = sshConfigPaths();
  const out = {
    source:    null,
    available: false,
    hosts:     [],
    note:      'IdentityFile is returned as a path reference only; key contents are never read by the renderer.',
  };
  for (const p of paths) {
    let text;
    try { text = fs.readFileSync(p, 'utf8'); }
    catch (e) {
      if (e.code !== 'ENOENT') {
        pushLog('warn', `ssh config read failed for ${p}: ${e.message}`);
      }
      continue;
    }
    out.source = p;
    out.available = true;
    const blocks = parseSshConfig(text);
    out.hosts = blocks
      .map((b) => ({
        alias:         b.alias,
        host:          b.hostName || b.alias,
        user:          b.user || '',
        port:          b.port || 22,
        identity_file: b.identityFile || '',
      }))
      .filter((h) => h.host && h.user); // skip entries without enough info
    break; // only the first existing config
  }
  return out;
}

function checkBearer(req) {
  const h = req.headers['authorization'] || req.headers['Authorization'];
  if (!h) return false;
  const m = /^Bearer\s+(\S+)$/i.exec(String(h));
  if (!m) return false;
  return m[1] === state.token;
}

