/** Fetch the decrypted credential secret for a context, if any.
 *  Returns the string or null. Main-process only — never leaves
 *  this file. */
function _credentialFor(ctx) {
  if (!ctx || !ctx.machine) return null;
  const ref = ctx.machine.credential_ref;
  if (!ref || !_credentialStore || typeof _credentialStore.get !== 'function') return null;
  try { return _credentialStore.get(ref); } catch (_) { return null; }
}

/** Normalize one record from `camc --json list` into the field shape
 *  Desktop renderer expects (web/js/desktop/shell.js +
 *  web/js/desktop/nodes-mode.js). camc emits a nested `task` object;
 *  we hoist `task.name → task_name`, `task.tool → tool`, etc., and
 *  stamp machine_host/user/port/type from the originating context so
 *  Nodes mode can group correctly. */
function _normalizeAgent(rec, ctx) {
  if (!rec || typeof rec !== 'object') return null;
  const t = (rec.task && typeof rec.task === 'object') ? rec.task : {};
  const m = (ctx && ctx.machine) || {};
  return {
    // Identity
    id:             String(rec.id || ''),
    session_id:     rec.session_id || '',
    // Task surface — flatten what shell.js / start-agent-mode read.
    task_name:      String(t.name || ''),
    tool:           String(t.tool || rec.tool || ''),
    prompt:         t.prompt || '',
    auto_confirm:   !!t.auto_confirm,
    auto_exit:      !!t.auto_exit,
    tags:           Array.isArray(t.tags) ? t.tags.slice(0, 32) : (Array.isArray(rec.tags) ? rec.tags.slice(0, 32) : []),
    // Context linkage — keep both names that downstream code uses.
    // Force `context_name` to the LOCAL ctx.name so duplicate sync
    // doesn't strand records under whatever stale value the remote
    // camc happened to report (ours vs theirs may diverge across
    // machines). Preserve the remote value separately for diagnostics.
    context_id:          rec.context_id || '',
    context_name:        (ctx && ctx.name) || '',
    remote_context_name: rec.context_name || '',
    context_path:        rec.context_path || rec.path || '',
    path:           rec.context_path || rec.path || '',
    transport_type: rec.transport_type || (m.type || 'local'),
    // Lifecycle.
    status:         rec.status || 'unknown',
    state:          rec.state  || '',
    tmux_session:   rec.tmux_session || rec.session || '',
    session:        rec.tmux_session || rec.session || '',
    tmux_socket:    rec.tmux_socket || '',
    tmux_bin:       rec.tmux_bin || (rec.runtime && rec.runtime.tmux && rec.runtime.tmux.bin) || '',
    pid:            rec.pid || null,
    hostname:       rec.hostname || '',
    created_at:      rec.created_at || t.created_at || null,
    updated_at:      rec.updated_at || t.updated_at || rec.last_active_at || t.last_active_at || rec.last_seen_at || t.last_seen_at || rec.activity_at || t.activity_at || rec.modified_at || t.modified_at || rec.mtime || t.mtime || null,
    last_active_at:  rec.last_active_at || t.last_active_at || null,
    last_seen_at:    rec.last_seen_at || t.last_seen_at || null,
    activity_at:     rec.activity_at || t.activity_at || null,
    modified_at:     rec.modified_at || t.modified_at || null,
    mtime:           rec.mtime || t.mtime || null,
    started_at:      rec.started_at || t.started_at || null,
    completed_at:    rec.completed_at || t.completed_at || null,
    exit_reason:     rec.exit_reason  || null,
    retry_count:    rec.retry_count  || 0,
    cost_estimate:  rec.cost_estimate || null,
    files_changed:  Array.isArray(rec.files_changed) ? rec.files_changed : [],
    // Stamp the originating node so Nodes-mode can group + filter.
    machine_host:   m.host || '',
    machine_user:   m.user || '',
    machine_port:   m.port || null,
    machine_type:   m.type || 'local',
  };
}

/** Replace every agent record matching this context with the freshly
 *  imported set. Dedupe is keyed on TWO signals:
 *    (1) any prior record whose `context_name` already equals ctxName,
 *    (2) any prior record whose (machine_host, id) matches one of the
 *        newly-imported agents.
 *  (1) cleans up agents that vanished from the remote. (2) cleans up
 *  stale rows that were imported earlier under a different
 *  `context_name` value (e.g. when remote camc reports a name like
 *  "ssh-test" that has since been renamed locally to "hren") — those
 *  rows would otherwise survive filter (1) and append duplicates.
 *  Agents in other contexts on other machines are untouched. */
function _contextEndpointKey(ctx) {
  const m = (ctx && ctx.machine) || {};
  const type = m.type || 'local';
  const port = type === 'ssh'
    ? String(m.port == null || m.port === '' ? 22 : m.port)
    : String(m.port == null ? '' : m.port);
  return `${type}|${m.user || ''}|${m.host || ''}|${port}`;
}

function _agentEndpointKey(a) {
  if (!a) return '';
  const type = a.machine_type || a.transport_type || 'local';
  const hasStampedEndpoint = !!(a.machine_host || a.machine_user || a.machine_port != null);
  if (hasStampedEndpoint) {
    const port = type === 'ssh'
      ? String(a.machine_port == null || a.machine_port === '' ? 22 : a.machine_port)
      : String(a.machine_port == null ? '' : a.machine_port);
    return `${type}|${a.machine_user || ''}|${a.machine_host || ''}|${port}`;
  }
  // Legacy rows written before machine_* stamping can still be matched
  // through their context_name. This lets a successful endpoint sync prune
  // stale failed/completed rows from old local stores.
  const resolvedCtx = _contextForAgentRecord(a);
  return resolvedCtx ? _contextEndpointKey(resolvedCtx) : '';
}

function _agentMatchesContextEndpoint(a, ctx) {
  if (!a || !ctx || !ctx.machine) return false;
  const ctxKey = _contextEndpointKey(ctx);
  const agentKey = _agentEndpointKey(a);
  return !!ctxKey && !!agentKey && ctxKey === agentKey;
}

function _upsertAgentsForContext(ctx, newAgents) {
  const ctxName = (ctx && ctx.name) || '';
  if (!state.store) state.store = _emptyStore();
  if (!Array.isArray(state.store.agents)) state.store.agents = [];
  const newKeys = new Set(newAgents.map(a => _agentDedupeKey(a)));
  const keep = state.store.agents.filter(a => {
    if ((a.context_name || '') === ctxName) return false;
    const k = _agentDedupeKey(a);
    if (k && newKeys.has(k)) return false;
    // Remote `camc list` is authoritative for the whole SSH endpoint,
    // not just one local context name. Remove same-host rows that vanished
    // remotely, including stale failed/completed rows imported under older
    // context names.
    if (_agentMatchesContextEndpoint(a, ctx)) return false;
    return true;
  });
  state.store.agents = keep.concat(newAgents);
  saveStore();
}

/** Run remote `camc --json list` (or fallback) over the injected
 *  ssh-transport and import any active agents into state.store.
 *
 *  `overrides` may carry `{password, passphrase}` for a one-shot
 *  sync that bypasses the credential store entirely. Used by the
 *  source `camui` CLI for debugging password-auth nodes without
 *  Electron `safeStorage`. The overrides are passed straight to
 *  the SSH transport (briefly in memory) and never persisted, never
 *  echoed in the response body, never written to the log buffer.
 *
 *  Returns one of:
 *    { ok: true,  imported, total, results: { camc: 'updated'|'unchanged' } }
 *    { ok: false, error, detail, results: { camc: 'failed' } }
 *
 *  Does not upload/deploy camc — only checks that `~/.cam/camc` exists on
 *  the remote. One-time deploy is the operator's job (`cam sync` on a
 *  workstation). Agent ops (send/key/stop) still use `_ensureRemoteCamc`.
 */
async function _syncContextAgents(ctx, overrides = {}) {
  if (!ctx || !ctx.machine || ctx.machine.type !== 'ssh') {
    return {
      ok:      false,
      error:   'not_ssh',
      detail:  `context "${ctx && ctx.name || '?'}" is not an SSH context`,
      results: { camc: 'failed' },
    };
  }
  if (!_sshTransport || typeof _sshTransport.execRemote !== 'function') {
    return {
      ok:      false,
      error:   'ssh_transport_unavailable',
      detail:  'embedded Hub has no SSH transport configured',
      results: { camc: 'failed' },
    };
  }
  const m = ctx.machine;
  const baseOpts = {
    host:        m.host,
    user:        m.user,
    port:        m.port || 22,
    auth_method: m.auth_method || (m.key_file ? 'key' : 'agent'),
    key_file:    m.key_file || '',
    timeout_ms:  SYNC_DEFAULT_TIMEOUT_MS,
  };
  // Decrypt the stored credential just before handing it to the
  // transport. Stays main-process-only. One-shot overrides (CLI
  // debug path) take precedence over the persisted credential and
  // are never written to disk.
  if (baseOpts.auth_method === 'password') {
    const pw = (typeof overrides.password === 'string' && overrides.password)
      ? overrides.password
      : _credentialFor(ctx);
    if (pw == null || pw === '') {
      return {
        ok:      false,
        error:   'credential_missing',
        detail:  'password auth is configured but no remembered or one-shot password is available',
        results: { camc: 'failed' },
      };
    }
    baseOpts.password = pw;
  } else if (baseOpts.auth_method === 'key') {
    const pp = (typeof overrides.passphrase === 'string' && overrides.passphrase)
      ? overrides.passphrase
      : _credentialFor(ctx);
    if (pp != null) baseOpts.passphrase = pp;
  }

  const ready = await _checkRemoteCamc(baseOpts);
  if (!ready.ok) {
    pushLog('warn', `sync ${ctx.name} failed: ${ready.error}`);
    return {
      ok:      false,
      error:   ready.error || 'camc_missing',
      detail:  ready.detail || 'remote ~/.cam/camc is not available',
      results: { camc: 'failed' },
    };
  }

  const res = await _sshTransport.execRemote({ ...baseOpts, command: `${REMOTE_CAMC} --json list` });
  if (!res || !res.ok) {
    const err = (res || {});
    let code = err.error || 'exec_failed';
    if (code === 'remote_nonzero' && /not found|No such file/i.test(err.detail || err.stderr || '')) {
      code = 'camc_missing';
    }
    pushLog('warn', `sync ${ctx.name} failed: ${code}`);
    return {
      ok:      false,
      error:   code,
      detail:  err.detail || err.stderr || 'unknown sync failure',
      results: { camc: 'failed' },
    };
  }

  let parsed;
  try { parsed = JSON.parse(res.stdout || '[]'); }
  catch (e) {
    pushLog('warn', `sync ${ctx.name}: invalid JSON from remote camc`);
    return {
      ok:      false,
      error:   'invalid_json',
      detail:  `remote camc did not return JSON: ${e && e.message}`,
      results: { camc: 'failed' },
    };
  }
  if (!Array.isArray(parsed)) {
    return {
      ok:      false,
      error:   'invalid_json',
      detail:  'remote camc returned a non-array JSON body',
      results: { camc: 'failed' },
    };
  }

  // Filter out clearly-broken records (no id) but otherwise import as-is.
  const normalized = parsed
    .map(r => _normalizeAgent(r, ctx))
    .filter(r => r && r.id);

  // Remember the previous count so we can report updated/unchanged.
  const prev = (state.store && state.store.agents)
    ? state.store.agents.filter(a => (a.context_name || '') === ctx.name)
    : [];
  _upsertAgentsForContext(ctx, normalized);

  // Mark context last_used_at so future sync deltas have a timestamp.
  if (state.store) {
    const idx = state.store.contexts.findIndex(c => c.name === ctx.name);
    if (idx >= 0) {
      state.store.contexts[idx].last_used_at = nowIso();
      saveStore();
    }
  }

  // results: 'updated' when the imported set differs in any way from
  // the previous snapshot, 'unchanged' otherwise. Order-independent
  // shallow comparison keyed on id+status+state.
  function fp(a) { return `${a.id}|${a.status}|${a.state}|${a.task_name}|${a.updated_at || ''}`; }
  const prevSig = new Set(prev.map(fp));
  const newSig  = new Set(normalized.map(fp));
  const same = (prevSig.size === newSig.size)
    && [...prevSig].every(k => newSig.has(k));
  const status = same ? 'unchanged' : 'updated';

  pushLog('info', `sync ${ctx.name}: ${status} (${normalized.length} agent(s))`);
  return {
    ok:       true,
    imported: normalized.length,
    total:    parsed.length,
    results:  { camc: status },
  };
}

function _syncableAgentContexts() {
  const ctxs = (state.store && Array.isArray(state.store.contexts)) ? state.store.contexts : [];
  const byEndpoint = new Map();
  for (const ctx of ctxs) {
    const m = (ctx && ctx.machine) || {};
    if ((m.type || 'local') !== 'ssh') continue;
    const key = `${m.user || ''}@${m.host || ''}:${m.port || 22}`;
    if (!byEndpoint.has(key)) byEndpoint.set(key, ctx);
  }
  return [...byEndpoint.values()];
}

async function _syncAllAgentContexts(reason = 'manual') {
  if (state.agentSyncInFlight) {
    return { ok: false, error: 'sync_in_flight', detail: 'agent sync already running' };
  }
  const contexts = _syncableAgentContexts();
  if (!contexts.length) return { ok: true, synced: 0, failed: 0, results: [] };
  state.agentSyncInFlight = true;
  const results = [];
  let synced = 0;
  let failed = 0;
  try {
    for (const ctx of contexts) {
      // eslint-disable-next-line no-await-in-loop
      const r = await _syncContextAgents(ctx);
      results.push({ context: ctx.name, ok: !!(r && r.ok), imported: r && r.imported || 0, error: r && r.error || null });
      if (r && r.ok) synced++;
      else failed++;
    }
    state.lastAgentSyncAt = Date.now();
    const prune = synced > 0
      ? _pruneUnownedStoreAgents(reason)
      : { removed: 0, ids: [] };
    const pruneNote = prune.removed ? `, pruned ${prune.removed} orphan local agent(s)` : '';
    pushLog(failed ? 'warn' : 'info', `agent sync ${reason}: ${synced} ok, ${failed} failed${pruneNote}`);
    return { ok: failed === 0, synced, failed, results, pruned: prune.removed, pruned_ids: prune.ids };
  } finally {
    state.agentSyncInFlight = false;
  }
}


/** Look up an agent and its owning context from the local store.
 *  Returns `{ agent, ctx }` or `{ error }` shape so callers can map
 *  the failure to a useful UI message instead of an empty body. */
function _contextForAgent(agentId) {
  if (!state.store) return { error: 'store_unavailable' };
  const id = decodeURIComponent(String(agentId || ''));
  const agents = Array.isArray(state.store.agents) ? state.store.agents : [];
  const candidates = agents.filter(a => a && (String(a.id || '') === id || String(a.id || '').startsWith(id)));
  if (!candidates.length) return { error: 'agent_not_found' };
  const ctxs = Array.isArray(state.store.contexts) ? state.store.contexts : [];
  for (const agent of candidates) {
    const byName = ctxs.find(c => c && c.name === agent.context_name);
    if (byName) return { agent, ctx: byName };
  }
  for (const agent of candidates) {
    const byMachine = ctxs.find((ctx) => {
      const m = (ctx && ctx.machine) || {};
      return (m.type || 'local') === (agent.machine_type || agent.transport_type || 'local')
        && (m.host || '') === (agent.machine_host || '')
        && (m.user || '') === (agent.machine_user || '')
        && String(m.port == null ? '' : m.port) === String(agent.machine_port == null ? '' : agent.machine_port);
    });
    if (byMachine) return { agent, ctx: byMachine };
  }
  return { error: 'context_not_found', agent: candidates[0] };
}

/** Run `~/.cam/camc <args>` on a remote context via the injected SSH
 *  transport. Bootstraps the bundled camc to that exact path first
 *  when the remote file is missing, not executable, or hash-different
 *  from the bundled copy.
 *  Returns `{ ok: true, stdout, stderr }` or `{ ok: false, error,
 *  detail }`. Pure execution helper — does not mutate the store. */
function _bundledCamcPath() {
  const candidates = [];
  if (process.resourcesPath) {
    candidates.push(path.join(process.resourcesPath, 'camc', 'camc'));
  }
  candidates.push(path.resolve(__dirname, '..', '..', '..', 'src', 'camc'));
  candidates.push(path.resolve(__dirname, '..', '..', '..', 'dist', 'camc'));
  for (const c of candidates) {
    try { if (fs.statSync(c).isFile()) return c; } catch (_) {}
  }
  return null;
}

function _readBundledCamc() {
  const p = _bundledCamcPath();
  if (!p) return { error: 'bundled_camc_missing', detail: 'bundled camc was not found in resources/camc/camc, src/camc, or dist/camc' };
  try {
    const content = fs.readFileSync(p);
    return {
      content,
      path: p,
      hash: crypto.createHash('md5').update(content).digest('hex').slice(0, 12),
    };
  }
  catch (e) { return { error: 'bundled_camc_read_failed', detail: e && e.message }; }
}

function _remoteCamcReadyKey(baseOpts, localHash) {
  const secret = baseOpts && (baseOpts.password || baseOpts.passphrase || '');
  const secretDigest = secret
    ? crypto.createHash('sha256').update(String(secret)).digest('hex').slice(0, 16)
    : '';
  return [
    baseOpts && baseOpts.host || '',
    baseOpts && baseOpts.user || '',
    String(baseOpts && baseOpts.port || 22),
    baseOpts && baseOpts.auth_method || '',
    baseOpts && baseOpts.key_file || '',
    secretDigest,
    localHash || '',
  ].join('|');
}

function _markRemoteCamcReady(baseOpts, localHash) {
  if (!state.remoteCamcReadyCache) state.remoteCamcReadyCache = new Map();
  state.remoteCamcReadyCache.set(_remoteCamcReadyKey(baseOpts, localHash), Date.now());
}

function _clearRemoteCamcReady(baseOpts, localHash) {
  if (!state.remoteCamcReadyCache) return;
  state.remoteCamcReadyCache.delete(_remoteCamcReadyKey(baseOpts, localHash));
}

/** Sync-only probe: verify remote camc exists. No upload/deploy (run `cam sync` once on a workstation if missing). */
async function _checkRemoteCamc(baseOpts) {
  if (!_sshTransport || typeof _sshTransport.execRemote !== 'function') {
    return {
      ok:     false,
      error:  'ssh_transport_unavailable',
      detail: 'embedded Hub has no SSH transport configured',
    };
  }
  const verify = await _sshTransport.execRemote({
    ...baseOpts,
    command: `test -x ${REMOTE_CAMC}`,
  });
  if (verify && verify.ok) {
    return { ok: true, present: true };
  }
  const host = `${baseOpts.user}@${baseOpts.host}:${baseOpts.port || 22}`;
  return {
    ok:     false,
    error:  'camc_missing',
    detail: `~/.cam/camc is not installed or not executable on ${host}. Deploy once with cam sync from a workstation, then retry Sync Host.`,
  };
}

async function _ensureRemoteCamc(baseOpts, { force = false } = {}) {
  // Test stubs used by source smokes may not implement uploads. In
  // that case, assume the stub has made camc available and keep the
  // older tests focused on list/capture behavior.
  if (!_sshTransport || typeof _sshTransport.writeRemoteFile !== 'function') {
    return { ok: true, skipped: 'writeRemoteFile_unavailable' };
  }

  const local = _readBundledCamc();
  if (local.error) return { ok: false, error: local.error, detail: local.detail };

  const readyKey = _remoteCamcReadyKey(baseOpts, local.hash);
  if (!force && state.remoteCamcReadyCache && state.remoteCamcReadyCache.has(readyKey)) {
    return { ok: true, present: true, cached: true, hash: local.hash };
  }

  const remoteHash = await _sshTransport.execRemote({
    ...baseOpts,
    command: `bash -c 'test -x ${REMOTE_CAMC} && md5sum ${REMOTE_CAMC} 2>/dev/null | cut -c1-12'`,
  });
  const remote = remoteHash && remoteHash.ok ? String(remoteHash.stdout || '').trim() : '';
  if (remote && remote === local.hash) {
    _markRemoteCamcReady(baseOpts, local.hash);
    return { ok: true, present: true, hash: remote };
  }

  const mkdir = await _sshTransport.execRemote({ ...baseOpts, command: 'mkdir -p ~/.cam' });
  if (!mkdir || !mkdir.ok) {
    return { ok: false, error: mkdir && mkdir.error || 'remote_mkdir_failed', detail: mkdir && (mkdir.detail || mkdir.stderr) || 'mkdir -p ~/.cam failed' };
  }

  const uploaded = await _sshTransport.writeRemoteFile({
    ...baseOpts,
    remotePath: REMOTE_CAMC_UPLOAD_PATH,
    content: local.content,
  });
  if (!uploaded || !uploaded.ok) {
    return { ok: false, error: uploaded && uploaded.error || 'remote_camc_upload_failed', detail: uploaded && uploaded.detail || 'failed to upload bundled camc' };
  }

  const install = await _sshTransport.execRemote({ ...baseOpts, command: `chmod 700 ${REMOTE_CAMC_UPLOAD_PATH} && mv ${REMOTE_CAMC_UPLOAD_PATH} ${REMOTE_CAMC}` });
  if (!install || !install.ok) {
    return { ok: false, error: install && install.error || 'remote_camc_install_failed', detail: install && (install.detail || install.stderr) || 'failed to install ~/.cam/camc' };
  }

  const verify = await _sshTransport.execRemote({ ...baseOpts, command: `test -x ${REMOTE_CAMC}` });
  if (!verify || !verify.ok) {
    return { ok: false, error: verify && verify.error || 'remote_camc_verify_failed', detail: verify && (verify.detail || verify.stderr) || '~/.cam/camc is not executable after upload' };
  }
  const action = remote ? 'updated' : 'installed';
  _markRemoteCamcReady(baseOpts, local.hash);
  pushLog('info', `${action} bundled camc on ${baseOpts.user}@${baseOpts.host}:${baseOpts.port || 22}`);
  return { ok: true, installed: !remote, updated: !!remote, hash: local.hash };
}

