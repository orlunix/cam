/**
 * CAM Desktop — embedded CAM Hub (CAM-DESK-DIRECT-010..019,
 * CAM-DESK-HUB-010..012).
 *
 * Implements the Direct connection target as a self-contained Node
 * HTTP server inside Electron main. NO host CAM runtime is required:
 * no `cam` CLI, no `python`, no WSL, no shell spawn. Everything is
 * stdlib (`http`, `crypto`, `fs`, `path`).
 *
 * Wire contract: a strict subset of the existing CAM API that the
 * renderer (`web/js/api.js`) already speaks. Endpoints implemented in
 * this first pass:
 *
 *   GET  /api/system/health       — CAM-shaped readiness, no auth
 *   GET  /api/system/config       — minimal config view, bearer auth
 *   GET  /api/contexts            — list contexts from app-data store
 *   GET  /api/contexts/:name      — 404 in this pass (registry empty)
 *   GET  /api/agents              — list agents from app-data store
 *   GET  /api/agents/:id          — 404 in this pass (registry empty)
 *   GET  /api/agents/:id/output?lines=N&format=ansi|plain — tail capture
 *   GET  /api/agents/:id/fulloutput?format=ansi|plain      — full scrollback
 *   GET  /api/agents/:id/logs     — empty logs shape
 *
 * Mutating endpoints (POST /api/agents, PATCH /api/agents/:id,
 * DELETE /api/agents/:id, /restart, and context-copy cleanup paths
 * still return 501 Not Implemented with a JSON error
 * body so the renderer can surface it cleanly. They will be wired in
 * a later pass that pins down DIRECT-015..018 (node management +
 * credential references) and a remote-controller transport.
 *
 * WebSocket (`/api/ws`) is intentionally omitted in this pass:
 * `CamApi._connectEventStream()` tolerates ws failure and the
 * renderer falls back to its existing 5s REST polling.
 *
 * Storage: a single JSON file at `<userData>/embedded-hub.json` with
 *   { version: 1, contexts: [], agents: [], adapters: [...] }
 * Loaded on first start and rewritten only when the registry grows.
 *
 * Auth: every /api/* endpoint except /api/system/health requires
 * `Authorization: Bearer <token>` where token is generated by
 * `crypto.randomBytes(24).toString('base64url')` on each Start.
 *
 * Lifecycle: a single owned server at 127.0.0.1:8420 (or first free
 * port in 8420..8429). Stop closes the server; restart = stop+start.
 * The main process is the only thing that can lifecycle this — the
 * renderer-facing surface lives in `preload.cjs` as
 * `CamBridge.directHub.*`.
 */

'use strict';

const http   = require('node:http');
const crypto = require('node:crypto');
const fs     = require('node:fs');
const os     = require('node:os');
const path   = require('node:path');

const DEFAULT_PORT       = 8420;
const PORT_SCAN_RANGE    = 10;
const LOG_BUFFER_LINES   = 200;
const DEFAULT_ADAPTERS   = ['claude', 'codex', 'cursor'];
const STORE_VERSION      = 1;
const HUB_PRODUCT_VERSION = 'cam-desktop-embedded-1';

/* ─────────────── State ─────────────── */

const state = {
  server:     null,
  port:       null,
  token:      null,
  startedAt:  0,
  owned:      false,
  logs:       [],   // ring buffer of recent events {kind,text,ts}
  lastError:  null,
  store:      null, // loaded JSON store
  dataDir:    null,
  storePath:  null,
};

function tokenFingerprint(tok) {
  if (!tok || typeof tok !== 'string') return null;
  return 'sha256:' + crypto.createHash('sha256').update(tok).digest('hex').slice(0, 24);
}

function genToken() {
  return crypto.randomBytes(24).toString('base64url');
}

function pushLog(kind, text) {
  const line = { kind, text: String(text || ''), ts: Date.now() };
  state.logs.push(line);
  if (state.logs.length > LOG_BUFFER_LINES) {
    state.logs.splice(0, state.logs.length - LOG_BUFFER_LINES);
  }
}

/* ─────────────── App-data store ─────────────── */

function _emptyStore() {
  return {
    version:  STORE_VERSION,
    contexts: [],
    agents:   [],
    adapters: DEFAULT_ADAPTERS.slice(),
  };
}

function loadStore(dataDir) {
  state.dataDir = dataDir;
  state.storePath = path.join(dataDir, 'embedded-hub.json');
  try {
    fs.mkdirSync(dataDir, { recursive: true });
  } catch (e) { pushLog('warn', `mkdir userData failed: ${e.message}`); }

  try {
    const raw = fs.readFileSync(state.storePath, 'utf8');
    const parsed = JSON.parse(raw);
    state.store = {
      ..._emptyStore(),
      ...parsed,
      adapters: Array.isArray(parsed.adapters) && parsed.adapters.length
        ? parsed.adapters
        : DEFAULT_ADAPTERS.slice(),
    };
    _repairStoreAgents();
  } catch (e) {
    if (e.code !== 'ENOENT') pushLog('warn', `store read failed: ${e.message}`);
    state.store = _emptyStore();
    try { fs.writeFileSync(state.storePath, JSON.stringify(state.store, null, 2)); }
    catch (e2) { pushLog('warn', `store init write failed: ${e2.message}`); }
  }
}

/** Atomically write the JSON store: write to a sibling .tmp file
 *  first, then rename. The rename is effectively atomic on POSIX
 *  and on Windows when the destination exists. */
function saveStore() {
  if (!state.storePath || !state.store) return;
  const tmp = state.storePath + '.tmp';
  try {
    fs.writeFileSync(tmp, JSON.stringify(state.store, null, 2));
    fs.renameSync(tmp, state.storePath);
  } catch (e) {
    pushLog('warn', `store write failed: ${e.message}`);
  }
}

function _agentDedupeKey(a) {
  if (!a || !a.id) return '';
  const type = a.machine_type || a.transport_type || 'local';
  const host = a.machine_host || '';
  const user = a.machine_user || '';
  const port = a.machine_port == null ? '' : String(a.machine_port);
  // Local records do not carry a meaningful remote endpoint; keep their
  // context in the key. SSH imports are deduped by endpoint+agent id so
  // stale rows imported under old remote context names are collapsed.
  const ctx = type === 'ssh' ? '' : (a.context_name || '');
  return `${type}|${user}|${host}|${port}|${ctx}|${a.id}`;
}

function _contextForAgentRecord(a) {
  const ctxs = (state.store && Array.isArray(state.store.contexts)) ? state.store.contexts : [];
  if (!a || !ctxs.length) return null;
  const byName = a.context_name ? ctxs.find(c => c && c.name === a.context_name) : null;
  if (byName) return byName;
  return ctxs.find((ctx) => {
    const m = (ctx && ctx.machine) || {};
    return (m.type || 'local') === (a.machine_type || a.transport_type || 'local')
      && (m.host || '') === (a.machine_host || '')
      && (m.user || '') === (a.machine_user || '')
      && String(m.port == null ? '' : m.port) === String(a.machine_port == null ? '' : a.machine_port);
  }) || null;
}

function _repairStoreAgents() {
  if (!state.store || !Array.isArray(state.store.agents)) return false;
  const out = [];
  const byKey = new Map();
  let changed = false;

  for (const raw of state.store.agents) {
    if (!raw || typeof raw !== 'object' || !raw.id) {
      out.push(raw);
      continue;
    }
    const ctx = _contextForAgentRecord(raw);
    let a = raw;
    if (ctx && raw.context_name !== ctx.name) {
      a = { ...raw };
      if (!a.remote_context_name && raw.context_name) a.remote_context_name = raw.context_name;
      a.context_name = ctx.name;
      if (!a.context_id && ctx.id) a.context_id = ctx.id;
      changed = true;
    }
    const key = _agentDedupeKey(a);
    if (!key) {
      out.push(a);
      continue;
    }
    const prior = byKey.get(key);
    if (prior != null) {
      out[prior] = a;
      changed = true;
    } else {
      byKey.set(key, out.length);
      out.push(a);
    }
  }

  if (changed) {
    const before = state.store.agents.length;
    state.store.agents = out;
    saveStore();
    pushLog('info', `agent registry repaired: ${before} -> ${out.length}`);
  }
  return changed;
}

/* ─────────────── HTTP helpers ─────────────── */

/** CORS headers for loopback-only Electron renderer.
 *
 * The renderer loads from a `file://` URL but `fetch()`es
 * `http://127.0.0.1:<port>/api/...` with an Authorization header.
 * Chromium treats that as a cross-origin request and emits an OPTIONS
 * preflight, so every response needs explicit CORS headers and OPTIONS
 * must answer 204. The Hub only ever binds to 127.0.0.1, so allowing
 * `*` here is acceptable: the socket itself is unreachable from
 * outside the host.
 */
function corsHeaders() {
  return {
    'Access-Control-Allow-Origin':  '*',
    'Access-Control-Allow-Headers': 'Authorization, Content-Type',
    'Access-Control-Allow-Methods': 'GET, POST, PUT, PATCH, DELETE, OPTIONS',
    'Access-Control-Max-Age':       '600',
    'Vary':                         'Origin',
  };
}

function sendJson(res, status, body) {
  const buf = Buffer.from(JSON.stringify(body), 'utf8');
  res.writeHead(status, {
    'Content-Type':   'application/json; charset=utf-8',
    'Content-Length': buf.length,
    'Cache-Control':  'no-store',
    ...corsHeaders(),
  });
  res.end(buf);
}

function send501(res, what) {
  sendJson(res, 501, {
    error:           'not_implemented',
    detail:          what,
    embedded_hub:    true,
    next_stage_req:  'CAM-DESK-DIRECT-015..018',
  });
}

function send404(res) {
  sendJson(res, 404, { error: 'not_found' });
}

function send400(res, detail, code) {
  // Default `error` code is `bad_request`; callers can pass a more
  // specific code (e.g. `safe_storage_unavailable`, `invalid_name`)
  // so renderers can switch on it instead of grepping `detail`.
  sendJson(res, 400, { error: code || 'bad_request', detail });
}

const REQ_BODY_MAX = 25 * 1024 * 1024; // upload payloads are base64 JSON

function readJsonBody(req) {
  return new Promise((resolve, reject) => {
    let size = 0;
    const chunks = [];
    req.on('data', (chunk) => {
      size += chunk.length;
      if (size > REQ_BODY_MAX) {
        reject(new Error('body_too_large'));
        try { req.destroy(); } catch {}
        return;
      }
      chunks.push(chunk);
    });
    req.on('end', () => {
      const text = Buffer.concat(chunks).toString('utf8');
      if (!text) return resolve({});
      try { resolve(JSON.parse(text)); }
      catch { reject(new Error('invalid_json')); }
    });
    req.on('error', reject);
  });
}

/* ─────────────── Context helpers (CAM-DESK-DIRECT-014..018) ─────────────── */

const NAME_RE      = /^[A-Za-z0-9_-]{1,64}$/;
const AUTH_METHODS = new Set(['key', 'password', 'agent']);

function nowIso() { return new Date().toISOString(); }

// Optional credential store injected by Electron main. The Node smoke
// can install a deterministic stub; the hub itself is unaware of the
// implementation. Absent → no Remember support (Remember=true with no
// store returns 400 below; non-Remember create works fine).
let _credentialStore = null;

// Optional SSH transport injected by Electron main (CAM-DESK-DIRECT-019).
// Same idea: hub is unaware of the implementation. The Node smoke
// installs a deterministic stub; in Electron main this is
// `ssh-transport.cjs` wrapping ssh2.
let _sshTransport = null;

// Remote agent-discovery command. Two-pass: prefer the embedded camc
// shim, fall back to the system `camc` on PATH (which is what the
// unified CAM/camc environment installs).
const SYNC_DEFAULT_TIMEOUT_MS = 15000;
const REMOTE_CAMC = '~/.cam/camc';
const REMOTE_CAMC_UPLOAD_PATH = '.cam/camc.tmp';

function configure({ credentialStore, sshTransport } = {}) {
  if (credentialStore !== undefined) _credentialStore = credentialStore || null;
  if (sshTransport    !== undefined) _sshTransport    = sshTransport    || null;
}

function _credentialAvailable() {
  return !!(_credentialStore && typeof _credentialStore.available === 'function' && _credentialStore.available());
}

function _normalizeAuthMethod(body, keyFile) {
  let m = body.auth_method == null ? '' : String(body.auth_method).trim().toLowerCase();
  if (!m) {
    // Reasonable default: if a key_file was supplied, assume 'key';
    // otherwise 'agent' (use ssh-agent / default key search).
    m = keyFile ? 'key' : 'agent';
  }
  if (!AUTH_METHODS.has(m)) return null;
  return m;
}

/** Helper: persist a single remembered secret for `(contextId, kind)`
 *  via the injected credential store. Returns { ok } or
 *  { error, detail }. Refuses to save when Remember was asked for
 *  but the store / safeStorage are unavailable — we never fall back
 *  to plaintext. */
function _saveRememberedSecret(contextId, kind, secret) {
  if (!_credentialStore || typeof _credentialStore.put !== 'function') {
    return { error: 'credential_store_unavailable', detail: 'Remember requires the Electron credential store.' };
  }
  if (!_credentialAvailable()) {
    return { error: 'safe_storage_unavailable', detail: 'OS keychain/safeStorage is not available; cannot remember secrets.' };
  }
  const res = _credentialStore.put(`${contextId}:${kind}`, kind, secret);
  if (!res || res.ok !== true) {
    return { error: res && res.error ? res.error : 'credential_save_failed' };
  }
  return { ok: true, ref: res.ref, kind, saved_at: res.saved_at };
}

function _cascadeDeleteCreds(contextId) {
  if (!_credentialStore || typeof _credentialStore.removeForContext !== 'function') return;
  try { _credentialStore.removeForContext(contextId); }
  catch (_) {}
}

function buildContextRecord(body) {
  const name = String(body.name || '').trim();
  if (!NAME_RE.test(name)) {
    return { error: 'invalid_name', detail: 'name must match [A-Za-z0-9_-]{1,64}' };
  }
  const ctxPath = String(body.path || '').trim();
  if (!ctxPath) {
    return { error: 'missing_path', detail: 'path is required' };
  }
  const host = body.host == null ? '' : String(body.host).trim();
  const user = body.user == null ? '' : String(body.user).trim();
  const port = body.port == null || body.port === ''
    ? null
    : Number.parseInt(body.port, 10);
  if (port != null && (!Number.isFinite(port) || port < 1 || port > 65535)) {
    return { error: 'invalid_port', detail: 'port must be 1..65535' };
  }
  const isSSH = !!host;
  if (isSSH && !user) {
    return { error: 'missing_user', detail: 'SSH contexts require host and user' };
  }
  const keyFile  = body.key_file  == null ? '' : String(body.key_file).trim();
  const envSetup = body.env_setup == null ? '' : String(body.env_setup).trim();
  const tags     = Array.isArray(body.tags)
    ? body.tags.filter(t => typeof t === 'string' && t).slice(0, 32)
    : [];

  const authMethod = isSSH ? _normalizeAuthMethod(body, keyFile) : null;
  if (isSSH && !authMethod) {
    return { error: 'invalid_auth_method', detail: 'auth_method must be one of: key, password, agent' };
  }

  // CAM-DESK-DIRECT-018 enforcement: a password-auth context with no
  // usable credential would be silently unsyncable. The Hub allows
  // exactly two valid shapes:
  //   1. remember_password=true (the normal GUI path; the password
  //      is persisted via safeStorage and the context owns its own
  //      credential).
  //   2. allow_one_shot=true (the source-CLI debug path; the
  //      context is created with credential_saved=false and the
  //      caller must pass `password` on every POST /sync. Useful
  //      when Electron safeStorage isn't available, e.g. the dev
  //      `camui` CLI.)
  // Anything else (auth_method=password with no remember and no
  // allow_one_shot) is refused at create time.
  const allowOneShot = !!body.allow_one_shot;
  if (isSSH && authMethod === 'password'
      && !body.remember_password && !allowOneShot) {
    return {
      error:  'missing_credential',
      detail: 'auth_method=password requires remember_password=true (GUI path) OR allow_one_shot=true (CLI debug path). The Hub will not create a context that has no way to authenticate.',
    };
  }

  const id = crypto.randomUUID();
  const machine = {
    type:       isSSH ? 'ssh' : 'local',
    host:       isSSH ? host : '',
    user:       isSSH ? user : '',
    port:       isSSH ? (port || 22) : null,
    key_file:   isSSH && authMethod === 'key' ? keyFile : '',
    env_setup:  envSetup,
  };
  if (isSSH) {
    machine.auth_method      = authMethod;       // 'key' | 'password' | 'agent'
    machine.credential_ref   = '';               // populated below if remembered
    machine.credential_kind  = '';               // 'password' | 'passphrase'
    machine.credential_saved = false;
    // Source-CLI debug marker: when true, the context was created
    // without a stored credential and Sync requires a one-shot
    // `password` (or `passphrase`) in the body. Useful for
    // safeStorage-less debug.
    if (allowOneShot) machine.allow_one_shot = true;
  }

  // Optional Remember support. Raw secret never makes it to the
  // record we return / persist — it goes to the credential store
  // (encrypted via safeStorage) and we only carry the ref/metadata.
  if (isSSH) {
    if (authMethod === 'password' && body.remember_password) {
      const secret = body.password == null ? '' : String(body.password);
      if (!secret) {
        return { error: 'missing_password', detail: 'remember_password=true requires a non-empty password' };
      }
      const saved = _saveRememberedSecret(id, 'password', secret);
      if (saved.error) return saved;
      machine.credential_ref   = saved.ref;
      machine.credential_kind  = 'password';
      machine.credential_saved = true;
    }
    if (authMethod === 'key' && body.remember_passphrase) {
      const secret = body.passphrase == null ? '' : String(body.passphrase);
      if (!secret) {
        return { error: 'missing_passphrase', detail: 'remember_passphrase=true requires a non-empty passphrase' };
      }
      const saved = _saveRememberedSecret(id, 'passphrase', secret);
      if (saved.error) return saved;
      machine.credential_ref   = saved.ref;
      machine.credential_kind  = 'passphrase';
      machine.credential_saved = true;
    }
  }

  return {
    record: {
      id,
      name,
      path:         ctxPath,
      machine,
      tags,
      created_at:   nowIso(),
      last_used_at: null,
    },
  };
}

function findContextByName(name) {
  if (!state.store) return null;
  return state.store.contexts.find(c => c.name === name) || null;
}

function applyContextUpdate(existing, body) {
  // Apply only known scalar fields; refuse to rewrite id/created_at.
  const next = { ...existing };
  if (body.path != null)       next.path = String(body.path).trim();
  if (body.tags != null && Array.isArray(body.tags)) {
    next.tags = body.tags.filter(t => typeof t === 'string' && t).slice(0, 32);
  }
  const m = { ...(existing.machine || {}) };
  if (body.host      != null) m.host      = String(body.host).trim();
  if (body.user      != null) m.user      = String(body.user).trim();
  if (body.key_file  != null) m.key_file  = String(body.key_file).trim();
  if (body.env_setup != null) m.env_setup = String(body.env_setup).trim();
  if (body.port      != null && body.port !== '') {
    const p = Number.parseInt(body.port, 10);
    if (!Number.isFinite(p) || p < 1 || p > 65535) {
      return { error: 'invalid_port', detail: 'port must be 1..65535' };
    }
    m.port = p;
  }
  m.type = m.host ? 'ssh' : 'local';
  if (m.type === 'ssh' && !m.user) {
    return { error: 'missing_user', detail: 'SSH contexts require host and user' };
  }

  // Auth method change tracking: when auth_method changes, drop the
  // previous credential ref. The new method's secret (if any) is
  // saved further below.
  let prevMethod = m.auth_method || '';
  let nextMethod = prevMethod;
  if (m.type === 'ssh') {
    if (body.auth_method != null) {
      const normalized = _normalizeAuthMethod({ auth_method: body.auth_method }, m.key_file);
      if (!normalized) {
        return { error: 'invalid_auth_method', detail: 'auth_method must be one of: key, password, agent' };
      }
      nextMethod = normalized;
    }
    if (!nextMethod) nextMethod = m.key_file ? 'key' : 'agent';
    if (nextMethod !== prevMethod) {
      _cascadeDeleteCreds(existing.id);
      m.credential_ref   = '';
      m.credential_kind  = '';
      m.credential_saved = false;
    }
    m.auth_method = nextMethod;
    // key_file only makes sense for the 'key' method.
    if (nextMethod !== 'key') m.key_file = '';
    // Same DIRECT-018 enforcement as create: a password-auth
    // context must either persist a credential now, already have
    // one, OR be explicitly marked as one-shot (CLI debug). Refuse
    // any other shape so we never silently end up with an
    // unsyncable record.
    if (nextMethod === 'password'
        && !body.remember_password
        && !m.credential_saved
        && !m.allow_one_shot
        && !body.allow_one_shot) {
      return {
        error:  'missing_credential',
        detail: 'auth_method=password requires remember_password=true OR allow_one_shot=true.',
      };
    }
    if (body.allow_one_shot === true) m.allow_one_shot = true;
  } else {
    delete m.auth_method;
    delete m.credential_ref;
    delete m.credential_kind;
    delete m.credential_saved;
  }

  // Optional Remember updates. Raw secrets never land in the record.
  if (m.type === 'ssh') {
    if (nextMethod === 'password' && body.remember_password) {
      const secret = body.password == null ? '' : String(body.password);
      if (!secret) {
        return { error: 'missing_password', detail: 'remember_password=true requires a non-empty password' };
      }
      const saved = _saveRememberedSecret(existing.id, 'password', secret);
      if (saved.error) return saved;
      m.credential_ref   = saved.ref;
      m.credential_kind  = 'password';
      m.credential_saved = true;
    } else if (nextMethod === 'key' && body.remember_passphrase) {
      const secret = body.passphrase == null ? '' : String(body.passphrase);
      if (!secret) {
        return { error: 'missing_passphrase', detail: 'remember_passphrase=true requires a non-empty passphrase' };
      }
      const saved = _saveRememberedSecret(existing.id, 'passphrase', secret);
      if (saved.error) return saved;
      m.credential_ref   = saved.ref;
      m.credential_kind  = 'passphrase';
      m.credential_saved = true;
    } else if (body.forget_credential === true) {
      _cascadeDeleteCreds(existing.id);
      m.credential_ref   = '';
      m.credential_kind  = '';
      m.credential_saved = false;
    }
  }

  next.machine = m;
  return { record: next };
}

/* ─────────────── Remote sync (CAM-DESK-DIRECT-016/019) ─────────────── */

/** Fetch the decrypted credential secret for a context, if any.
 *  Returns the string or null. Main-process only — never leaves
 *  this file. */
function _credentialFor(ctx) {
  if (!ctx || !ctx.machine) return null;
  const ref = ctx.machine.credential_ref;
  if (!ref || !_credentialStore || typeof _credentialStore.get !== 'function') return null;
  try { return _credentialStore.get(ref); } catch (_) { return null; }
}

/** Normalize one record from `camc --json list` into the field shape
 *  Desktop renderer expects (web/js/desktop/shell.js +
 *  web/js/desktop/nodes-mode.js). camc emits a nested `task` object;
 *  we hoist `task.name → task_name`, `task.tool → tool`, etc., and
 *  stamp machine_host/user/port/type from the originating context so
 *  Nodes mode can group correctly. */
function _normalizeAgent(rec, ctx) {
  if (!rec || typeof rec !== 'object') return null;
  const t = (rec.task && typeof rec.task === 'object') ? rec.task : {};
  const m = (ctx && ctx.machine) || {};
  return {
    // Identity
    id:             String(rec.id || ''),
    session_id:     rec.session_id || '',
    // Task surface — flatten what shell.js / start-agent-mode read.
    task_name:      String(t.name || ''),
    tool:           String(t.tool || rec.tool || ''),
    prompt:         t.prompt || '',
    auto_confirm:   !!t.auto_confirm,
    auto_exit:      !!t.auto_exit,
    tags:           Array.isArray(t.tags) ? t.tags.slice(0, 32) : (Array.isArray(rec.tags) ? rec.tags.slice(0, 32) : []),
    // Context linkage — keep both names that downstream code uses.
    // Force `context_name` to the LOCAL ctx.name so duplicate sync
    // doesn't strand records under whatever stale value the remote
    // camc happened to report (ours vs theirs may diverge across
    // machines). Preserve the remote value separately for diagnostics.
    context_id:          rec.context_id || '',
    context_name:        (ctx && ctx.name) || '',
    remote_context_name: rec.context_name || '',
    context_path:        rec.context_path || rec.path || '',
    path:           rec.context_path || rec.path || '',
    transport_type: rec.transport_type || (m.type || 'local'),
    // Lifecycle.
    status:         rec.status || 'unknown',
    state:          rec.state  || '',
    tmux_session:   rec.tmux_session || rec.session || '',
    session:        rec.tmux_session || rec.session || '',
    tmux_socket:    rec.tmux_socket || '',
    pid:            rec.pid || null,
    hostname:       rec.hostname || '',
    created_at:      rec.created_at || t.created_at || null,
    updated_at:      rec.updated_at || t.updated_at || rec.last_active_at || t.last_active_at || rec.last_seen_at || t.last_seen_at || rec.activity_at || t.activity_at || rec.modified_at || t.modified_at || rec.mtime || t.mtime || null,
    last_active_at:  rec.last_active_at || t.last_active_at || null,
    last_seen_at:    rec.last_seen_at || t.last_seen_at || null,
    activity_at:     rec.activity_at || t.activity_at || null,
    modified_at:     rec.modified_at || t.modified_at || null,
    mtime:           rec.mtime || t.mtime || null,
    started_at:      rec.started_at || t.started_at || null,
    completed_at:    rec.completed_at || t.completed_at || null,
    exit_reason:     rec.exit_reason  || null,
    retry_count:    rec.retry_count  || 0,
    cost_estimate:  rec.cost_estimate || null,
    files_changed:  Array.isArray(rec.files_changed) ? rec.files_changed : [],
    // Stamp the originating node so Nodes-mode can group + filter.
    machine_host:   m.host || '',
    machine_user:   m.user || '',
    machine_port:   m.port || null,
    machine_type:   m.type || 'local',
  };
}

/** Replace every agent record matching this context with the freshly
 *  imported set. Dedupe is keyed on TWO signals:
 *    (1) any prior record whose `context_name` already equals ctxName,
 *    (2) any prior record whose (machine_host, id) matches one of the
 *        newly-imported agents.
 *  (1) cleans up agents that vanished from the remote. (2) cleans up
 *  stale rows that were imported earlier under a different
 *  `context_name` value (e.g. when remote camc reports a name like
 *  "ssh-test" that has since been renamed locally to "hren") — those
 *  rows would otherwise survive filter (1) and append duplicates.
 *  Agents in other contexts on other machines are untouched. */
function _upsertAgentsForContext(ctxName, newAgents) {
  if (!state.store) state.store = _emptyStore();
  if (!Array.isArray(state.store.agents)) state.store.agents = [];
  const newKeys = new Set(newAgents.map(a => _agentDedupeKey(a)));
  const keep = state.store.agents.filter(a => {
    if ((a.context_name || '') === ctxName) return false;
    const k = _agentDedupeKey(a);
    if (k && newKeys.has(k)) return false;
    return true;
  });
  state.store.agents = keep.concat(newAgents);
  saveStore();
}

/** Run remote `camc --json list` (or fallback) over the injected
 *  ssh-transport and import any active agents into state.store.
 *
 *  `overrides` may carry `{password, passphrase}` for a one-shot
 *  sync that bypasses the credential store entirely. Used by the
 *  source `camui` CLI for debugging password-auth nodes without
 *  Electron `safeStorage`. The overrides are passed straight to
 *  the SSH transport (briefly in memory) and never persisted, never
 *  echoed in the response body, never written to the log buffer.
 *
 *  Returns one of:
 *    { ok: true,  imported, total, results: { camc: 'updated'|'unchanged' } }
 *    { ok: false, error, detail, results: { camc: 'failed' } }
 */
async function _syncContextAgents(ctx, overrides = {}) {
  if (!ctx || !ctx.machine || ctx.machine.type !== 'ssh') {
    return {
      ok:      false,
      error:   'not_ssh',
      detail:  `context "${ctx && ctx.name || '?'}" is not an SSH context`,
      results: { camc: 'failed' },
    };
  }
  if (!_sshTransport || typeof _sshTransport.execRemote !== 'function') {
    return {
      ok:      false,
      error:   'ssh_transport_unavailable',
      detail:  'embedded Hub has no SSH transport configured',
      results: { camc: 'failed' },
    };
  }
  const m = ctx.machine;
  const baseOpts = {
    host:        m.host,
    user:        m.user,
    port:        m.port || 22,
    auth_method: m.auth_method || (m.key_file ? 'key' : 'agent'),
    key_file:    m.key_file || '',
    timeout_ms:  SYNC_DEFAULT_TIMEOUT_MS,
  };
  // Decrypt the stored credential just before handing it to the
  // transport. Stays main-process-only. One-shot overrides (CLI
  // debug path) take precedence over the persisted credential and
  // are never written to disk.
  if (baseOpts.auth_method === 'password') {
    const pw = (typeof overrides.password === 'string' && overrides.password)
      ? overrides.password
      : _credentialFor(ctx);
    if (pw == null || pw === '') {
      return {
        ok:      false,
        error:   'credential_missing',
        detail:  'password auth is configured but no remembered or one-shot password is available',
        results: { camc: 'failed' },
      };
    }
    baseOpts.password = pw;
  } else if (baseOpts.auth_method === 'key') {
    const pp = (typeof overrides.passphrase === 'string' && overrides.passphrase)
      ? overrides.passphrase
      : _credentialFor(ctx);
    if (pp != null) baseOpts.passphrase = pp;
  }

  const ready = await _ensureRemoteCamc(baseOpts);
  if (!ready.ok) {
    pushLog('warn', `sync ${ctx.name} failed: ${ready.error}`);
    return {
      ok:      false,
      error:   ready.error || 'remote_camc_unavailable',
      detail:  ready.detail || 'failed to prepare ~/.cam/camc on remote host',
      results: { camc: 'failed' },
    };
  }

  const res = await _sshTransport.execRemote({ ...baseOpts, command: `${REMOTE_CAMC} --json list` });
  if (!res || !res.ok) {
    const err = (res || {});
    let code = err.error || 'exec_failed';
    if (code === 'remote_nonzero' && /not found|No such file/i.test(err.detail || err.stderr || '')) {
      code = 'camc_missing';
    }
    pushLog('warn', `sync ${ctx.name} failed: ${code}`);
    return {
      ok:      false,
      error:   code,
      detail:  err.detail || err.stderr || 'unknown sync failure',
      results: { camc: 'failed' },
    };
  }

  let parsed;
  try { parsed = JSON.parse(res.stdout || '[]'); }
  catch (e) {
    pushLog('warn', `sync ${ctx.name}: invalid JSON from remote camc`);
    return {
      ok:      false,
      error:   'invalid_json',
      detail:  `remote camc did not return JSON: ${e && e.message}`,
      results: { camc: 'failed' },
    };
  }
  if (!Array.isArray(parsed)) {
    return {
      ok:      false,
      error:   'invalid_json',
      detail:  'remote camc returned a non-array JSON body',
      results: { camc: 'failed' },
    };
  }

  // Filter out clearly-broken records (no id) but otherwise import as-is.
  const normalized = parsed
    .map(r => _normalizeAgent(r, ctx))
    .filter(r => r && r.id);

  // Remember the previous count so we can report updated/unchanged.
  const prev = (state.store && state.store.agents)
    ? state.store.agents.filter(a => (a.context_name || '') === ctx.name)
    : [];
  _upsertAgentsForContext(ctx.name, normalized);

  // Mark context last_used_at so future sync deltas have a timestamp.
  if (state.store) {
    const idx = state.store.contexts.findIndex(c => c.name === ctx.name);
    if (idx >= 0) {
      state.store.contexts[idx].last_used_at = nowIso();
      saveStore();
    }
  }

  // results: 'updated' when the imported set differs in any way from
  // the previous snapshot, 'unchanged' otherwise. Order-independent
  // shallow comparison keyed on id+status+state.
  function fp(a) { return `${a.id}|${a.status}|${a.state}|${a.task_name}|${a.updated_at || ''}`; }
  const prevSig = new Set(prev.map(fp));
  const newSig  = new Set(normalized.map(fp));
  const same = (prevSig.size === newSig.size)
    && [...prevSig].every(k => newSig.has(k));
  const status = same ? 'unchanged' : 'updated';

  pushLog('info', `sync ${ctx.name}: ${status} (${normalized.length} agent(s))`);
  return {
    ok:       true,
    imported: normalized.length,
    total:    parsed.length,
    results:  { camc: status },
  };
}

/** Look up an agent and its owning context from the local store.
 *  Returns `{ agent, ctx }` or `{ error }` shape so callers can map
 *  the failure to a useful UI message instead of an empty body. */
function _contextForAgent(agentId) {
  if (!state.store) return { error: 'store_unavailable' };
  const id = decodeURIComponent(String(agentId || ''));
  const agents = Array.isArray(state.store.agents) ? state.store.agents : [];
  const candidates = agents.filter(a => a && (String(a.id || '') === id || String(a.id || '').startsWith(id)));
  if (!candidates.length) return { error: 'agent_not_found' };
  const ctxs = Array.isArray(state.store.contexts) ? state.store.contexts : [];
  for (const agent of candidates) {
    const byName = ctxs.find(c => c && c.name === agent.context_name);
    if (byName) return { agent, ctx: byName };
  }
  for (const agent of candidates) {
    const byMachine = ctxs.find((ctx) => {
      const m = (ctx && ctx.machine) || {};
      return (m.type || 'local') === (agent.machine_type || agent.transport_type || 'local')
        && (m.host || '') === (agent.machine_host || '')
        && (m.user || '') === (agent.machine_user || '')
        && String(m.port == null ? '' : m.port) === String(agent.machine_port == null ? '' : agent.machine_port);
    });
    if (byMachine) return { agent, ctx: byMachine };
  }
  return { error: 'context_not_found', agent: candidates[0] };
}

/** Run `~/.cam/camc <args>` on a remote context via the injected SSH
 *  transport. Bootstraps the bundled camc to that exact path first
 *  when the remote file is missing, not executable, or hash-different
 *  from the bundled copy.
 *  Returns `{ ok: true, stdout, stderr }` or `{ ok: false, error,
 *  detail }`. Pure execution helper — does not mutate the store. */
function _bundledCamcPath() {
  const candidates = [];
  if (process.resourcesPath) {
    candidates.push(path.join(process.resourcesPath, 'camc', 'camc'));
  }
  candidates.push(path.resolve(__dirname, '..', '..', '..', 'src', 'camc'));
  candidates.push(path.resolve(__dirname, '..', '..', '..', 'dist', 'camc'));
  for (const c of candidates) {
    try { if (fs.statSync(c).isFile()) return c; } catch (_) {}
  }
  return null;
}

function _readBundledCamc() {
  const p = _bundledCamcPath();
  if (!p) return { error: 'bundled_camc_missing', detail: 'bundled camc was not found in resources/camc/camc, src/camc, or dist/camc' };
  try {
    const content = fs.readFileSync(p);
    return {
      content,
      path: p,
      hash: crypto.createHash('md5').update(content).digest('hex').slice(0, 12),
    };
  }
  catch (e) { return { error: 'bundled_camc_read_failed', detail: e && e.message }; }
}

async function _ensureRemoteCamc(baseOpts) {
  // Test stubs used by source smokes may not implement uploads. In
  // that case, assume the stub has made camc available and keep the
  // older tests focused on list/capture behavior.
  if (!_sshTransport || typeof _sshTransport.writeRemoteFile !== 'function') {
    return { ok: true, skipped: 'writeRemoteFile_unavailable' };
  }

  const local = _readBundledCamc();
  if (local.error) return { ok: false, error: local.error, detail: local.detail };

  const remoteHash = await _sshTransport.execRemote({
    ...baseOpts,
    command: `bash -c 'test -x ${REMOTE_CAMC} && md5sum ${REMOTE_CAMC} 2>/dev/null | cut -c1-12'`,
  });
  const remote = remoteHash && remoteHash.ok ? String(remoteHash.stdout || '').trim() : '';
  if (remote && remote === local.hash) {
    return { ok: true, present: true, hash: remote };
  }

  const mkdir = await _sshTransport.execRemote({ ...baseOpts, command: 'mkdir -p ~/.cam' });
  if (!mkdir || !mkdir.ok) {
    return { ok: false, error: mkdir && mkdir.error || 'remote_mkdir_failed', detail: mkdir && (mkdir.detail || mkdir.stderr) || 'mkdir -p ~/.cam failed' };
  }

  const uploaded = await _sshTransport.writeRemoteFile({
    ...baseOpts,
    remotePath: REMOTE_CAMC_UPLOAD_PATH,
    content: local.content,
  });
  if (!uploaded || !uploaded.ok) {
    return { ok: false, error: uploaded && uploaded.error || 'remote_camc_upload_failed', detail: uploaded && uploaded.detail || 'failed to upload bundled camc' };
  }

  const install = await _sshTransport.execRemote({ ...baseOpts, command: `chmod 700 ${REMOTE_CAMC_UPLOAD_PATH} && mv ${REMOTE_CAMC_UPLOAD_PATH} ${REMOTE_CAMC}` });
  if (!install || !install.ok) {
    return { ok: false, error: install && install.error || 'remote_camc_install_failed', detail: install && (install.detail || install.stderr) || 'failed to install ~/.cam/camc' };
  }

  const verify = await _sshTransport.execRemote({ ...baseOpts, command: `test -x ${REMOTE_CAMC}` });
  if (!verify || !verify.ok) {
    return { ok: false, error: verify && verify.error || 'remote_camc_verify_failed', detail: verify && (verify.detail || verify.stderr) || '~/.cam/camc is not executable after upload' };
  }
  const action = remote ? 'updated' : 'installed';
  pushLog('info', `${action} bundled camc on ${baseOpts.user}@${baseOpts.host}:${baseOpts.port || 22}`);
  return { ok: true, installed: !remote, updated: !!remote, hash: local.hash };
}

