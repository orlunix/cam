/* ─────────────── Router ─────────────── */

async function handle(req, res) {
  const { method } = req;
  const url = new URL(req.url, 'http://127.0.0.1');
  const p = url.pathname;

  // Preflight: Chromium emits OPTIONS for any /api/* fetch that
  // includes Authorization or a non-simple method. Answer 204 + CORS
  // headers; bearer auth is enforced on the real request.
  if (method === 'OPTIONS') {
    res.writeHead(204, corsHeaders());
    return res.end();
  }

  // health: no auth (CamApi connect probes /api/contexts with auth, but
  // some operator tools probe /health anonymously).
  if (method === 'GET' && p === '/api/system/health') {
    return sendJson(res, 200, healthBody());
  }

  // Every other /api/* needs bearer auth.
  if (p.startsWith('/api/')) {
    if (!checkBearer(req)) {
      pushLog('warn', `${method} ${p} 401`);
      return sendJson(res, 401, { error: 'unauthorized' });
    }
  } else {
    // No non-/api/* surface in this pass.
    return send404(res);
  }

  // /api/system/config
  if (method === 'GET' && p === '/api/system/config') {
    return sendJson(res, 200, {
      version:  HUB_PRODUCT_VERSION,
      data_dir: state.dataDir || null,
    });
  }

  // /api/system/ssh-config — read-only suggestion list for the Nodes
  // import UI. Returns parsed entries from the user's `~/.ssh/config`
  // (CAM-DESK-DIRECT-017). Never reads private-key contents — key
  // file values are returned as paths only.
  if (method === 'GET' && p === '/api/system/ssh-config') {
    try {
      return sendJson(res, 200, sshConfigHosts());
    } catch (e) {
      pushLog('warn', `ssh-config read failed: ${e.message}`);
      return sendJson(res, 200, { available: false, source: null, hosts: [], note: 'ssh config not readable' });
    }
  }

  // WebSocket request (CamApi attempts /api/ws?token=...). We don't
  // implement WS in this pass; return 426 so the renderer's event
  // stream fallback closes cleanly and polling continues.
  if (p === '/api/ws') {
    res.writeHead(426, {
      'Content-Type': 'application/json; charset=utf-8',
      'Connection':   'close',
    });
    return res.end(JSON.stringify({ error: 'upgrade_required', detail: 'WS deferred in embedded Hub' }));
  }

  // Skillm library management (CAM-DESK-SKILLM-010..014).
  // Renderer supplies context names only; SSH credentials stay inside
  // Electron main and route through the same pooled ssh2 transport as camc.
  if (p === '/api/skillm/status' && method === 'GET') {
    const out = await _skillmStatus(url.searchParams.get('context') || '');
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/repos' && method === 'GET') {
    const out = await _skillmRepos(url.searchParams.get('context') || '');
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/list' && method === 'GET') {
    const out = await _skillmList(url.searchParams.get('context') || '', {
      repoName: url.searchParams.get('repo') || '',
      sync: url.searchParams.get('sync') === '1',
    });
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/repos' && method === 'POST') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmRepoAdd(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/repos' && method === 'PATCH') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmRepoUpdate(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/repos' && method === 'DELETE') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmRepoRemove(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/repos/refresh' && method === 'POST') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmRepoRefresh(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/repo-connect' && method === 'POST') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmRepoConnect(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/sync' && method === 'POST') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmSync(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }
  if (p === '/api/skillm/install' && method === 'POST') {
    let body;
    try { body = await readJsonBody(req); }
    catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
    const out = await _skillmInstall(body || {});
    return sendJson(res, out.httpStatus, out.body);
  }

  // Contexts — CRUD against the embedded store
  // (CAM-DESK-DIRECT-014..016).
  if (p === '/api/contexts') {
    if (method === 'GET') {
      return sendJson(res, 200, { contexts: state.store.contexts });
    }
    if (method === 'POST') {
      let body;
      try { body = await readJsonBody(req); }
      catch (e) { return send400(res, e.message); }
      // Pre-check dup name BEFORE buildContextRecord. buildContextRecord
      // calls _saveRememberedSecret() when remember_password /
      // remember_passphrase is true, which would persist a credential
      // blob whose ref nothing ever references — orphaning it on disk.
      // We still let buildContextRecord report invalid_name for shape
      // problems, but only after we've confirmed the name is free.
      const requestedName = body && body.name != null ? String(body.name).trim() : '';
      if (requestedName && NAME_RE.test(requestedName) && findContextByName(requestedName)) {
        return sendJson(res, 400, { error: 'duplicate_name', detail: `context "${requestedName}" already exists` });
      }
      const built = buildContextRecord(body || {});
      if (built.error) return send400(res, built.detail || built.error, built.error);
      // Re-check the dup case after build in case of a TOCTOU race —
      // single-process server with no async between the pre-check and
      // here, so this is belt-and-suspenders.
      if (findContextByName(built.record.name)) {
        // Roll back any credential blob the build path might have just
        // saved for this rejected record.
        _cascadeDeleteCreds(built.record.id);
        return sendJson(res, 400, { error: 'duplicate_name', detail: `context "${built.record.name}" already exists` });
      }
      state.store.contexts.push(built.record);
      saveStore();
      pushLog('info', `context created: ${built.record.name}`);
      return sendJson(res, 201, built.record);
    }
    return send404(res);
  }
  const ctxMatch = /^\/api\/contexts\/([^/]+)(\/.*)?$/.exec(p);
  if (ctxMatch) {
    const ctxName = decodeURIComponent(ctxMatch[1]);
    const sub = ctxMatch[2] || '';
    const existing = findContextByName(ctxName);

    if (method === 'GET' && !sub) {
      if (!existing) return send404(res);
      return sendJson(res, 200, existing);
    }
    if ((method === 'PUT' || method === 'PATCH') && !sub) {
      if (!existing) return send404(res);
      let body;
      try { body = await readJsonBody(req); }
      catch (e) { return send400(res, e.message); }
      const upd = applyContextUpdate(existing, body || {});
      if (upd.error) return send400(res, upd.detail || upd.error, upd.error);
      const idx = state.store.contexts.findIndex(c => c.name === ctxName);
      state.store.contexts[idx] = upd.record;
      saveStore();
      pushLog('info', `context updated: ${ctxName}`);
      return sendJson(res, 200, upd.record);
    }
    if (method === 'DELETE' && !sub) {
      if (!existing) return send404(res);
      const idx = state.store.contexts.findIndex(c => c.name === ctxName);
      state.store.contexts.splice(idx, 1);
      saveStore();
      // Cascade: drop any remembered password/passphrase tied to
      // this context id so they don't linger as orphans in the
      // credential store (CAM-DESK-DIRECT-018).
      _cascadeDeleteCreds(existing.id);
      pushLog('info', `context deleted: ${ctxName}`);
      return sendJson(res, 200, { ok: true });
    }
    if (method === 'POST' && sub === '/sync') {
      if (!existing) return send404(res);
      // Optional one-shot credential overrides for debugging via the
      // source `camui` CLI (where Electron safeStorage isn't
      // available). Body is optional; renderer never sends one.
      // Overrides are kept in memory only for the duration of the
      // SSH handshake; not persisted, not logged, not echoed.
      let body = {};
      const ctype = (req.headers['content-type'] || '').toLowerCase();
      if (ctype.includes('application/json')) {
        try { body = await readJsonBody(req); }
        catch (e) { return send400(res, e.message); }
      }
      const overrides = {};
      if (typeof body.password   === 'string' && body.password)   overrides.password   = body.password;
      if (typeof body.passphrase === 'string' && body.passphrase) overrides.passphrase = body.passphrase;
      const result = await _syncContextAgents(existing, overrides);
      // Always 200: the renderer's existing per-context tally relies
      // on `results` + an `ok` flag, not on HTTP status. Auth/connect
      // errors become results.camc='failed' + an `error` code the UI
      // can show as a warning toast.
      return sendJson(res, 200, result);
    }
    if (method === 'POST' && sub === '/copy') return send501(res, 'context copy');
    if (method === 'GET'   && sub === '/files') {
      const subpath = url.searchParams.get('path') || '';
      const out = await _browseContextList(existing.name, subpath);
      return sendJson(res, out.httpStatus, out.body);
    }
    if (method === 'GET'   && sub === '/files/read') {
      const subpath = url.searchParams.get('path') || '';
      const out = await _browseContextRead(existing.name, subpath);
      return sendJson(res, out.httpStatus, out.body);
    }
    return send404(res);
  }

  // Agents
  if (p === '/api/agents') {
    if (method === 'GET') {
      const refresh = /^(1|true|yes|sync)$/i.test(url.searchParams.get('refresh') || '');
      let sync = null;
      if (refresh) sync = await _syncAllAgentContexts('api-refresh');
      _repairStoreAgents();
      return sendJson(res, 200, {
        agents: state.store.agents,
        sync_in_flight: !!state.agentSyncInFlight,
        last_sync_at: state.lastAgentSyncAt || null,
        sync,
      });
    }
    if (method === 'POST') return send501(res, 'start agent');
    return send404(res);
  }
  const agentMatch = /^\/api\/agents\/([^/]+)(\/.*)?$/.exec(p);
  if (agentMatch) {
    const sub = agentMatch[2] || '';
    if (method === 'GET' && !sub)                          return send404(res);
    if (method === 'PATCH' && !sub) {
      let body = {};
      const ctype = (req.headers['content-type'] || '').toLowerCase();
      if (ctype.includes('application/json')) {
        try { body = await readJsonBody(req); }
        catch (e) { return send400(res, e.message); }
      }
      const result = await _editAgent(agentMatch[1], body || {});
      if (!result.ok) {
        const bad = new Set(['nothing_to_update', 'invalid_args', 'invalid_tag', 'invalid_name', 'unsupported_tool', 'system_prompt_too_large', 'workspace_missing', 'path_traversal']);
        const status = result.error === 'agent_not_found'
          ? 404
          : (bad.has(result.error) ? 400 : 502);
        return sendJson(res, status, { error: result.error, detail: result.detail });
      }
      return sendJson(res, 200, { ok: true, agent: result.agent || null });
    }
    if (method === 'DELETE' && !sub) {
      const force = /^(1|true|yes)$/i.test(url.searchParams.get('force') || '');
      const stopped = await _stopAgent(agentMatch[1], { force });
      if (!stopped.ok) {
        const status = stopped.error === 'agent_not_found' ? 404
                     : stopped.error === 'context_not_found' ? 404
                     : stopped.error === 'store_unavailable' ? 503
                     : 502;
        return sendJson(res, status, { error: stopped.error || 'stop_failed', detail: stopped.detail });
      }
      return sendJson(res, 200, { ok: true, agent: stopped.agent || null, no_op: !!stopped.no_op });
    }
    if (method === 'DELETE' && sub === '/history') {
      const removed = await _removeAgent(agentMatch[1]);
      if (!removed.ok) {
        const status = removed.error === 'agent_not_found' ? 404
                     : removed.error === 'context_not_found' ? 404
                     : removed.error === 'store_unavailable' ? 503
                     : 502;
        return sendJson(res, status, { error: removed.error || 'remove_failed', detail: removed.detail });
      }
      return sendJson(res, 200, { ok: true, removed_id: removed.removed_id });
    }
    if (sub === '/cron') {
      if (method === 'GET') {
        const result = await _listAgentCron(agentMatch[1]);
        if (!result.ok) return sendJson(res, _cronHttpStatus(result.error), { error: result.error, detail: result.detail });
        return sendJson(res, 200, result);
      }
      if (method === 'POST') {
        let body;
        try { body = await readJsonBody(req); }
        catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
        const result = await _addAgentCron(agentMatch[1], body || {});
        if (!result.ok) return sendJson(res, _cronHttpStatus(result.error), { error: result.error, detail: result.detail });
        return sendJson(res, 201, result);
      }
    }
    if (sub && sub.startsWith('/cron/')) {
      if (method === 'DELETE') {
        const key = sub.slice('/cron/'.length);
        const result = await _removeAgentCron(agentMatch[1], key);
        if (!result.ok) return sendJson(res, _cronHttpStatus(result.error), { error: result.error, detail: result.detail });
        return sendJson(res, 200, result);
      }
    }
    if (method === 'POST'  && sub === '/restart')          return send501(res, 'restart agent');
    if (method === 'POST'  && sub === '/input') {
      let body;
      try { body = await readJsonBody(req); }
      catch (e) { return send400(res, e.message); }
      const text = body && body.text != null ? String(body.text) : '';
      const sendEnter = body && body.send_enter === false ? false : true;
      if (!text) return send400(res, 'text is required', 'missing_text');
      const sent = await _sendAgentInput(agentMatch[1], text, sendEnter);
      if (!sent.ok) {
        const detail = _sendLogDetail(sent);
        pushLog('warn', `input ${agentMatch[1]} failed: ${sent.error}${detail ? `: ${detail}` : ''}`);
        return sendJson(res, 502, { error: sent.error || 'send_failed', detail: sent.detail || 'send failed' });
      }
      _clearOutputCaptureCache(agentMatch[1]);
      return sendJson(res, 200, { ok: true });
    }
    if (method === 'POST'  && sub === '/key') {
      let body;
      try { body = await readJsonBody(req); }
      catch (e) { return send400(res, e.message); }
      const key = body && body.key != null ? String(body.key) : '';
      const sent = await _sendAgentKey(agentMatch[1], key);
      if (!sent.ok) {
        pushLog('warn', `key ${agentMatch[1]} failed: ${sent.error}`);
        const bad = new Set(['missing_key', 'invalid_key', 'agent_not_running', 'not_ssh', 'credential_missing']);
        const status = sent.error === 'agent_not_found' ? 404 : (bad.has(sent.error) ? 400 : 502);
        return sendJson(res, status, { error: sent.error || 'send_key_failed', detail: sent.detail || 'send key failed' });
      }
      _clearOutputCaptureCache(agentMatch[1]);
      return sendJson(res, 200, { ok: true });
    }
    if (method === 'POST'  && sub === '/upload') {
      let body;
      try { body = await readJsonBody(req); }
      catch (e) { return send400(res, e.message, e.message === 'body_too_large' ? 'body_too_large' : 'bad_request'); }
      const uploaded = await _uploadAgentFile(agentMatch[1], body || {});
      if (!uploaded.ok) {
        pushLog('warn', `upload ${agentMatch[1]} failed: ${uploaded.error}`);
        const bad = new Set(['missing_filename', 'missing_data', 'invalid_base64', 'working_dir_missing', 'agent_not_running', 'not_ssh', 'credential_missing']);
        const status = uploaded.error === 'agent_not_found' ? 404 : (bad.has(uploaded.error) ? 400 : 502);
        return sendJson(res, status, { error: uploaded.error || 'upload_failed', detail: uploaded.detail || 'upload failed' });
      }
      _clearOutputCaptureCache(agentMatch[1]);
      return sendJson(res, 200, uploaded);
    }
    if (method === 'GET'   && sub === '/logs')             return sendJson(res, 200, { logs: '', truncated: false });
    if (method === 'GET'   && sub === '/output') {
      const id = agentMatch[1];
      const lines = Math.max(0, parseInt(url.searchParams.get('lines') || '50', 10) || 50);
      const format = (url.searchParams.get('format') === 'ansi') ? 'ansi' : 'plain';
      const resolved = _contextForAgent(id);
      if (resolved.error) {
        return sendJson(res, 502, {
          error:  resolved.error,
          detail: `Output unavailable: ${resolved.error}`,
        });
      }
      const captured = await _captureAgentOutput(resolved, id, lines, format);
      if (!captured.ok) {
        pushLog('warn', `output ${id} via ${resolved.ctx.name} failed: ${captured.error}`);
        return sendJson(res, 502, {
          error:  captured.error,
          detail: `Output unavailable: ${captured.detail || captured.error}`,
        });
      }
      const clientHash = url.searchParams.get('hash') || '';
      if (clientHash && clientHash === captured.hash) {
        return sendJson(res, 200, { unchanged: true, hash: captured.hash, lines: captured.lines });
      }
      return sendJson(res, 200, { output: captured.output, hash: captured.hash, lines: captured.lines });
    }
    if (method === 'GET'   && sub === '/fulloutput') {
      const id = agentMatch[1];
      const format = (url.searchParams.get('format') === 'ansi') ? 'ansi' : 'plain';
      const resolved = _contextForAgent(id);
      if (resolved.error) {
        return sendJson(res, 502, {
          error:  resolved.error,
          detail: `Output unavailable: ${resolved.error}`,
        });
      }
      // lines=0 → full scrollback per `camc capture` default.
      const captured = await _captureAgentOutput(resolved, id, 0, format);
      if (!captured.ok) {
        pushLog('warn', `fulloutput ${id} via ${resolved.ctx.name} failed: ${captured.error}`);
        return sendJson(res, 502, {
          error:  captured.error,
          detail: `Output unavailable: ${captured.detail || captured.error}`,
        });
      }
      return sendJson(res, 200, { output: captured.output, offset: 0 });
    }
    if (method === 'GET' && sub === '/workspace/files') {
      const subpath = url.searchParams.get('path') || '';
      const out = await _browseAgentList(agentMatch[1], subpath);
      return sendJson(res, out.httpStatus, out.body);
    }
    if (method === 'GET' && sub === '/workspace/files/read') {
      const subpath = url.searchParams.get('path') || '';
      const out = await _browseAgentRead(agentMatch[1], subpath);
      return sendJson(res, out.httpStatus, out.body);
    }
    if (method === 'POST' && sub === '/workspace/files/write') {
      let body;
      try { body = await readJsonBody(req); }
      catch (e) { return send400(res, e.message); }
      const subpath = body && body.path != null ? String(body.path) : '';
      const out = await _browseAgentWrite(agentMatch[1], subpath, body && body.content);
      return sendJson(res, out.httpStatus, out.body);
    }
    return send404(res);
  }

  return send404(res);
}

/* ─────────────── Port discovery ─────────────── */

function probePort(port) {
  return new Promise((resolve) => {
    const probe = http.createServer();
    probe.once('error', (err) => {
      const code = err && (err.code || err.message) || 'listen_error';
      resolve({
        port,
        state: code === 'EADDRINUSE' ? 'busy' : 'unavailable',
        error: String(code),
      });
    });
    probe.once('listening', () => {
      probe.close(() => resolve({ port, state: 'free' }));
    });
    probe.listen({ host: '127.0.0.1', port });
  });
}

async function portInUse(port) {
  const p = await probePort(port);
  return p.state !== 'free';
}

async function scanPortCandidates() {
  const out = [];
  for (let p = DEFAULT_PORT; p < DEFAULT_PORT + PORT_SCAN_RANGE; p++) {
    // eslint-disable-next-line no-await-in-loop
    out.push(await probePort(p));
  }
  return out;
}

function summarizePortCandidates(candidates) {
  const summary = {
    count:       candidates.length,
    free:        0,
    busy:        0,
    unavailable: 0,
    firstFree:   null,
    errors:      {},
  };
  for (const c of candidates) {
    if (!c) continue;
    if (c.state === 'free') {
      summary.free += 1;
      if (summary.firstFree == null) summary.firstFree = c.port;
    } else if (c.state === 'busy') {
      summary.busy += 1;
    } else {
      summary.unavailable += 1;
    }
    if (c.error) summary.errors[c.error] = (summary.errors[c.error] || 0) + 1;
  }
  return summary;
}

async function pickPort() {
  for (let p = DEFAULT_PORT; p < DEFAULT_PORT + PORT_SCAN_RANGE; p++) {
    // eslint-disable-next-line no-await-in-loop
    if (!(await portInUse(p))) return p;
  }
  return null;
}

function portRangeInfo() {
  return {
    start: DEFAULT_PORT,
    end:   DEFAULT_PORT + PORT_SCAN_RANGE - 1,
    count: PORT_SCAN_RANGE,
  };
}

/* ─────────────── Lifecycle ─────────────── */

async function start({ dataDir, apiToken } = {}) {
  if (state.server) {
    return {
      ok:        true,
      apiUrl:    `http://127.0.0.1:${state.port}`,
      apiToken:  state.token,
      reused:    true,
      state:     publicState(),
    };
  }

  if (dataDir && !state.store) loadStore(dataDir);
  if (!state.store) state.store = _emptyStore();

  const preferredPort = await pickPort();
  const listenPort = preferredPort == null ? 0 : preferredPort;
  if (preferredPort == null) {
    pushLog('warn', `No free preferred loopback port in ${DEFAULT_PORT}..${DEFAULT_PORT + PORT_SCAN_RANGE - 1}; trying OS-assigned fallback port`);
  }

  state.token = apiToken ? String(apiToken) : genToken();
  state.port  = null;

  const srv = http.createServer(async (req, res) => {
    try {
      await handle(req, res);
    } catch (e) {
      pushLog('error', `route error: ${e && e.message}`);
      try { sendJson(res, 500, { error: 'internal_error' }); } catch {}
    }
  });

  srv.on('clientError', (err, sock) => {
    pushLog('warn', `clientError: ${err && err.message}`);
    try { sock.destroy(); } catch {}
  });

  await new Promise((resolve, reject) => {
    srv.once('error', reject);
    srv.listen({ host: '127.0.0.1', port: listenPort }, () => resolve());
  }).catch((e) => {
    const target = listenPort === 0 ? 'OS-assigned loopback port' : `127.0.0.1:${listenPort}`;
    state.lastError = `listen failed on ${target}: ${e && e.message}`;
    pushLog('error', state.lastError);
  });

  if (!srv.listening) {
    state.token = null;
    state.port  = null;
    return { ok: false, error: state.lastError || 'listen failed', state: publicState() };
  }

  const addr = srv.address();
  const actualPort = addr && typeof addr === 'object' && addr.port ? addr.port : listenPort;
  state.port      = actualPort;
  state.server    = srv;
  state.startedAt = Date.now();
  state.owned     = true;
  state.lastError = null;
  pushLog('info', `embedded Hub listening on 127.0.0.1:${actualPort}`);

  return {
    ok:       true,
    apiUrl:   `http://127.0.0.1:${actualPort}`,
    apiToken: state.token,
    state:    publicState(),
  };
}

async function stop() {
  if (!state.server) {
    return { ok: true, state: publicState() };
  }
  await new Promise((resolve) => {
    try { state.server.close(() => resolve()); }
    catch { resolve(); }
  });
  state.server    = null;
  state.startedAt = 0;
  state.owned     = false;
  // Token is intentionally cleared on Stop so the renderer cannot
  // reuse a stale credential against a different process listening
  // on the same port later.
  state.token     = null;
  pushLog('info', 'embedded Hub stopped');
  return { ok: true, state: publicState() };
}

async function restart({ dataDir } = {}) {
  await stop();
  return start({ dataDir });
}

async function check({ dataDir } = {}) {
  if (dataDir && !state.store) loadStore(dataDir);

  let summary;
  let probedPort;
  let portState;

  if (state.server) {
    // We're already up. Report the port we own.
    probedPort = state.port;
    portState  = 'embedded-hub';
    summary    = 'running';
  } else {
    // Mirror what start() would actually do: scan the candidate range
    // and pick the first free loopback port. Reporting 'port-conflict'
    // only when ALL candidates are taken keeps the UI Start button
    // enabled when the default 8420 is busy but 8421+ is free, which
    // is the behavior start() would take anyway.
    const candidates  = await scanPortCandidates();
    const candidateSummary = summarizePortCandidates(candidates);
    const startable   = candidateSummary.firstFree;
    const defaultBusy = candidates[0] && candidates[0].state !== 'free';
    if (startable == null) {
      const fallback = await probePort(0);
      candidateSummary.fallback = fallback.state === 'free' ? 'free' : 'unavailable';
      if (fallback.error) {
        candidateSummary.fallbackError = fallback.error;
        candidateSummary.errors[fallback.error] = (candidateSummary.errors[fallback.error] || 0) + 1;
      }
      if (fallback.state === 'free') {
        probedPort = 0;
        portState  = 'fallback-free';
        summary    = 'stopped';
      } else {
        probedPort = DEFAULT_PORT;
        portState  = candidateSummary.unavailable > 0 ? 'unavailable' : 'other';
        summary    = 'port-conflict';
      }
    } else {
      probedPort = startable;
      portState  = defaultBusy ? 'alternate-free' : 'free';
      summary    = 'stopped';
    }
    state.lastPortCandidates = candidateSummary;
  }

  return {
    platform:       process.platform,
    runtime:        'embedded',
    apiPort:        probedPort,
    apiPortRange:   portRangeInfo(),
    apiPortCandidates: state.lastPortCandidates || null,
    apiPortStatus:  { state: portState },
    summary,
    state:          publicState(),
    lastError:      state.lastError || null,
  };
}

function publicState() {
  return {
    server: {
      owned:            state.owned,
      pid:              state.server ? process.pid : null,
      port:             state.port,
      startedAt:        state.startedAt || null,
      tokenFingerprint: tokenFingerprint(state.token),
    },
  };
}

function getLogs() {
  // Return the same shape the renderer's diag panel expects: { server: [...] }.
  return { server: state.logs.slice() };
}

function getProfile() {
  return {
    apiUrl:  state.port ? `http://127.0.0.1:${state.port}` : null,
    state:   publicState(),
  };
}

/* ─────────────── Exports ─────────────── */

/**
 * Build the SSH connect options the main process needs to attach a
 * terminal channel to <agentId>. Resolves the agent's owning context,
 * pulls the (decrypted) password/passphrase out of the credential
 * store if remembered, and returns a plain object suitable for
 * sshTransport.openTerminalChannel(). Secrets stay in main-process
 * memory — this helper is not exposed via IPC or preload.
 *
 * Returns one of:
 *   { ok: true,  agent, ctx, opts }
 *   { ok: false, error, detail }
 */
function _machineMatchesAgent(ctx, agent) {
  const m = (ctx && ctx.machine) || {};
  return (m.type || 'local') === (agent.machine_type || agent.transport_type || 'local')
    && (m.host || '') === (agent.machine_host || '')
    && (m.user || '') === (agent.machine_user || '')
    && String(m.port == null ? '' : m.port) === String(agent.machine_port == null ? '' : agent.machine_port);
}

function _attachContextForAgent(agent, fallbackCtx) {
  const ctxs = Array.isArray(state.store && state.store.contexts) ? state.store.contexts : [];
  const byMachine = ctxs.find(ctx => _machineMatchesAgent(ctx, agent));
  if (byMachine) return byMachine;
  const byName = agent && agent.context_name ? ctxs.find(c => c && c.name === agent.context_name) : null;
  return byName || fallbackCtx || null;
}

async function getAttachConnectOpts(agentId) {
  const resolved = _contextForAgent(agentId);
  if (resolved.error) {
    return { ok: false, error: resolved.error, detail: `agent ${agentId}: ${resolved.error}` };
  }
  const agent = resolved.agent || {};
  const ctx = _attachContextForAgent(agent, resolved.ctx);
  if (!ctx) return { ok: false, error: 'context_not_found', detail: `agent ${agentId}: context_not_found` };

  // Match `cam attach`: the live agent machine fields are the source of
  // truth for where the agent actually runs. The context supplies auth
  // metadata/credentials, but stale context host/user/port must not redirect
  // an attach to the wrong endpoint. Do not pre-filter by agent status here;
  // `camc attach` has the authoritative tmux-session check and returns the
  // useful stale-session error when needed.
  const built = _sshBaseOptsForContext(ctx, 60000);
  if (built.error) return { ok: false, error: built.error, detail: built.detail };
  const opts = { ...built.opts };
  if ((agent.machine_type || agent.transport_type || 'ssh') !== 'ssh') {
    return { ok: false, error: 'not_ssh', detail: `agent ${agentId}: terminal attach requires an SSH-backed agent` };
  }
  if (agent.machine_host) opts.host = agent.machine_host;
  if (agent.machine_user) opts.user = agent.machine_user;
  if (agent.machine_port != null && agent.machine_port !== '') opts.port = agent.machine_port;

  const ready = await _ensureRemoteCamc(opts);
  if (!ready.ok) {
    return { ok: false, error: ready.error || 'remote_camc_unavailable', detail: ready.detail || 'failed to prepare ~/.cam/camc on remote host' };
  }
  return {
    ok: true,
    agent,
    ctx,
    opts,
    command: `${REMOTE_CAMC} attach ${_shellQuote(agent.id || agentId)}`,
  };
}

